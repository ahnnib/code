#include <iostream>
#include <map>
using namespace std;

const int N = 1e5;
const long long BASE = 311;
const long long MOD = 1e9 + 7;

long long h[N + 5];
long long power[N + 5];

long long geth(int l, int r)
{
    return ((h[r] - (h[l-1] * power[r - l + 1]) % MOD + MOD) % MOD);
}

int main()
{
    string s;
    cin >> s;

    bool a[123] = {}; for (int i = 97; i <= 122; i++) cin >> a[i];

    int k;
    cin >> k;

    int n = (int) s.size();

    power[0] = 1;
    for (int i = 1; i <= n; i++) power[i] = power[i-1] * BASE % MOD;

    h[0] = 0;
    for (int i = 1; i <= n; i++)
        h[i] = (h[i-1] * BASE + s[i-1] - 'a' + 1) % MOD;

    map<int, bool> mark;
    for (int i = 0; i < n; i++)
    {
        int sai = 0;
        for (int j = i; j < n; j++)
        {
            if (sai > k) break;
            if (s[j] == 0) sai++;
            // if (!mark[geth(i, j)]) cout << s.substr(i, j - i + 1) << '\n';
            mark[geth(i, j)] = 1;
        }
    }

    cout << mark.size();
}